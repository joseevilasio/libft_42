void	*ft_memchr(char *s, int c, unsigned int n)
{
	c = c % 256;
	if (n > 0)
	{
		while (n-- > 0)
		{
			if (*s == c)
				return (s);
			s++;
		}
	}
	return ((void *) 0);
}
