#include "libft.h"

char	*ft_strrchr(char *s, int c)
{
	int	i;

	i = ft_strlen(s) + 1;
	c = c % 256;
	while (i-- > 0)
	{
		if (s[i] == c)
			return (s + i);
	}
	return (NULL);
}
